package siceyunsuan;

public class yunsuan {
    private int a;
    private int b;

    public void setA(int a) {
        this.a = a;
    }
    public void setB(int b) {
        this.b = b;
    }

    public int getA() {
        return a;
    }

    public int getB() {
        return b;
    }

    public int add(int a, int b){
        return a+b;
    }
    public int minus(int a,int b){
        return a-b;
    }
    public int times(int a,int b){
        return a*b;
    }
    public int divided(int a,int b){
        return a/b;
    }
}
